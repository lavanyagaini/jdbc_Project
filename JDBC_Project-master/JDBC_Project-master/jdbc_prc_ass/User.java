package jdbc_prc_ass;

public class User {
	private int id;
	private String username;
	private String email;
	private String password;
	private String address;
	private String facebookpwd;
	private String instapwd;
	private String snappwd;
	private String whatsapppwd;
	private String twitterpwd;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getFacebookpwd() {
		return facebookpwd;
	}
	public void setFacebookpwd(String facebookpwd) {
		this.facebookpwd = facebookpwd;
	}
	public String getInstapwd() {
		return instapwd;
	}
	public void setInstapwd(String instapwd) {
		this.instapwd = instapwd;
	}
	public String getSnappwd() {
		return snappwd;
	}
	public void setSnappwd(String snappwd) {
		this.snappwd = snappwd;
	}
	public String getWhatsapppwd() {
		return whatsapppwd;
	}
	public void setWhatsapppwd(String whatsapppwd) {
		this.whatsapppwd = whatsapppwd;
	}
	public String getTwitterpwd() {
		return twitterpwd;
	}
	public void setTwitterpwd(String twitterpwd) {
		this.twitterpwd = twitterpwd;
	}


}
